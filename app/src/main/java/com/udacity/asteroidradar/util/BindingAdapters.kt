@BindingAdapter("pictureOfDay")
fun bindPictureOfDay(imageView: ImageView, picture: PictureOfDay?) {
    picture?.let {
        if (picture.mediaType == "image") {
            val imgUri = picture.url.toUri().buildUpon().scheme("https").build()
            val context = imageView.context
            Glide.with(context)
                .load(imgUri)
                .into(imageView)
            imageView.contentDescription = context.getString(
                R.string.nasa_picture_of_day_content_description_format,
                picture.title
            )
        }
    }
}

@BindingAdapter("pictureOfDayTitle")
fun bindPictureOfDayTitle(textView: TextView, picture: PictureOfDay?) {
    picture?.let {
        if (picture.mediaType == "image") {
            textView.text = picture.title
        }
    }
}